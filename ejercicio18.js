// 18. Capturar un número y decir si es negativo o positivo.
function verificarPositivoONegativo(numero) {
    if (numero < 0) {
        console.log("Número negativo");
    } else if (numero > 0) {
        console.log("Número positivo");
    } else {
        console.log("El número es cero");
    }
}